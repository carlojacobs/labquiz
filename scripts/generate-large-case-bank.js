async function main() {
  const note = await fs.readFile(casePath, "utf8");
  const currentBank = parseCaseBankNote(note);
  const cases = buildCases();
  const bank = {
    labRows: currentBank.labRows,
    cases,
  };

  const validationBank = JSON.parse(JSON.stringify(bank));
  hydrateCasePanels(validationBank);
  validateCaseBank(validationBank);

  const output = `# LabQuiz Case Bank

This note is the single source of truth for the quiz. Edit the JSON block below to add lab rows, cases, answer options, or hints. The app reads this file on each \`/api/cases\` request.

Content rule of thumb: cases should be solvable primarily from the lab pattern. Clinical hints can add context, but the answer should not depend on imaging, cultures, specialist tests, or history that is absent from the fixed table. Avoid copy-paste severity: abnormal values in the same category should vary between cases unless a repeated value is intentional.

Generated case count: ${cases.length}. To regenerate this broad teaching bank, run \`npm run generate:cases\`.

\`\`\`json
${JSON.stringify(bank, null, 2)}
\`\`\`
`;

  await fs.writeFile(casePath, output);
  console.log(`Generated ${cases.length} cases in ${path.relative(process.cwd(), casePath)}.`);
}

function buildCases() {
  const cases = [];

  for (const profile of PROFILES) {
    for (let index = 0; index < CASES_PER_PROFILE; index += 1) {
      const variant = VARIANTS[index];

      cases.push({
        id: `${profile.slug}-${variant.slug}`,
        answer: profile.answer,
        explanation: `${profile.explanation} This ${variant.label} varies the severity and companion markers while keeping the same broad lab conclusion.`,
        labs: profile.labs(index),
        hints: buildHints(profile, variant),
        panels: profile.panels,
      });
    }
  }

  if (cases.length !== 365) {
    throw new Error(`Expected 365 generated cases, received ${cases.length}.`);
  }

  return cases;
}

function buildHints(profile, variant) {
  return [
    variant.hint,
    ...profile.hints,
  ].slice(0, 5);
}

function profile(slug, answer, panels, labs, explanation, hints) {
  return { slug, answer, panels, labs, explanation, hints };
}

function lab(value, flag = null) {
  return flag ? { value: String(value), flag } : { value: String(value) };
}

function high(values, index) {
  return lab(pick(values, index), "H");
}

function low(values, index) {
  return lab(pick(values, index), "L");
}

function normal(values, index) {
  return lab(pick(values, index));
}

function pick(values, index) {
  return values[index % values.length];
}

